#include "ros/ros.h"
#include "message_utils/Message.h"
#include "message_utils/message_utils.h"


void doMsg(const message_utils::Message::ConstPtr& msg_p){
    MP::message_print(*msg_p);
}
int main(int argc, char  *argv[])
{
    setlocale(LC_ALL,"");
    //2.初始化 ROS 节点:命名(唯一)
    ros::init(argc,argv,"listener");
    //3.实例化 ROS 句柄
    ros::NodeHandle nh;

    //4.实例化 订阅者 对象
    ros::Subscriber sub = nh.subscribe<message_utils::Message>("/uav1/message",10,doMsg);
    //5.处理订阅的消息(回调函数)

    //     6.设置循环调用回调函数
    ros::spin();//循环读取接收的数据，并调用回调函数处理

    return 0;
}